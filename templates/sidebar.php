      <H2 style="MARGIN-BOTTOM: 0px; PADDING-BOTTOM: 0px">Guide to Using the AU 
      Template </H2>
      <H4>Full Header with Sidebar</H4>
      <P>All the text and link formatting and layout spacing on this page are 
      controlled by Cascading Style Sheets (CSS). These means that none of the 
      code to style text and links is included on this page, but in a .css file 
      is linked to this document via a &lt;link&gt; tag in the header. This 
      method enforces standards across the Auburn University website and 
      provides for very easy web page creation for content providers.</P>
      <P>It's recommended that you learn a little more about CSS before using 
      this template. This way, you can learn how to use style attributes on a 
      individual page basis and how to override the master style sheet is 
      needed. Here are a few links:</P>
      <UL>
        <LI><A href="http://www.w3schools.com/css/default.asp">W3schools.com - 
        CSS Tutorial</A> 
        <LI><A href="http://www.yourhtmlsource.com/stylesheets/">HTMLsource - 
        Stylesheets</A> </LI></UL>
      <H2>Content Area</H2>
      <P>All text typed in this content area is set to the font family of 
      Verdana, Arial, Helvetica, sans-serif with the size of 0.75 em. A "font 
      family" means that the browser will use the first font in that list that 
      your web visitor has (i.e. if the visitor doesn't Verdana, the browser 
      will use Arial). All of the fonts are common and nearly 100% of users will 
      have Verdana. Just like in a word processing application, you can 
      <STRONG>bold</STRONG> and <EM>italicize</EM> words.</P>
      <P>There is a buffer of 6 pixels set for the left and right of the content 
      area, along with 8 pixels at the top and bottom. If you want to remove it, 
      put <EM>style="padding:0px"</EM> in the &lt;td&gt; tag of this content 
      area table cell. You can also increase/decrease the padding by replacing 
      "0" with a number, perferably between 1 and 10.</P>
      <P>All links are formatted to look <A 
      href="http://www.onedanshow.com/">like this</A>. These can also be 
      overridden using the style attribute.</P>
      <P>Here are examples of what the HTML header codes (&lt;h1&gt;, 
      &lt;h2&gt;, and &lt;h3&gt;) are formatted to look like:</P>
      <H1>Header Size Example 1 &lt;H1&gt;</H1>
      <H2>Header Size Example 2 &lt;H2&gt;</H2>
      <H3>Header Size Example 3 &lt;H3&gt;</H3>
      <P>You can remove the dashed line by adding <EM>style="border:none;"</EM> 
      to the tag. Solid and dotted lines are also possible. You can use 
      &lt;h4&gt; for a subtitle. There is no formatting placed on pictures 
      (&lt;img&gt;) or lists (&lt;ul&gt;).</P>
      <H2>Top Links</H2>
      <P>These links are for subsections or services (i.e. site index, 
      directory, etc.) provided by your site. If you look at the concept for the 
      <A href="http://www.auburn.edu/includes/template_homepage.html">AU 
      Homepage</A>, you will see that these links provide drop down boxes for a 
      search box and quick links.</P>
      <H2>College or Department Title</H2>
      <P>This title is an image and must be created using graphics software. 
      It's perferred that each college or department use a unique image to put 
      behind their title. The Office of Communications and Marketing will assist 
      you in this process.</P>
      <H2>Toolbar Links</H2>
      <P>These links are for the main sections of your site. For an example, 
      look at the concept for the <A 
      href="http://www.auburn.edu/includes/template_homepage.html">AU 
      homepage</A>. If you have more or fewer than five toolbar links, you will 
      need to add or remove columns from the orange HTML table. In Dreamweaver, 
      you can do this in the Design View by right clicking on the last toolbar 
      link, selecting <EM>Table</EM>, and then selecting <EM>Insert Column</EM> 
      or <EM>Delete Column</EM>. </P>
      <H2>Sidebar Area</H2>
      <P>These links are for pages within a section of your site. Add or remove 
      links as needed, but it's recommended to stay within the background 
      picture of the Samford clock as much as possible.</P>
      <P>Please note that the sidebar links may not appear in a column in your 
      HTML editor (i.e. Dreamweaver) and you'll need to test the page in a 
      browser.</P>
